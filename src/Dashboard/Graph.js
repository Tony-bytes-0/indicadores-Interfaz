import React from 'react';
import { Chart as ChartJS, ArcElement, Tooltip, Legend } from 'chart.js';
import { Pie } from 'react-chartjs-2';
import { Grid, Typography } from '@mui/material';

ChartJS.register(ArcElement, Tooltip, Legend);
//generate the format of the object necesari to build the Pie component
function createGraph(params){
  return {
    labels: params.labels,
    datasets: [{
        label:params.datasets[0].label, data:params.datasets[0].data,
        //static data         
        backgroundColor: [
            'rgba(255, 99, 132, 0.2)',
            'rgba(54, 162, 235, 0.2)',
            'rgba(255, 206, 86, 0.2)',
            'rgba(75, 192, 192, 0.2)',
            'rgba(153, 102, 255, 0.2)',
            'rgba(255, 159, 64, 0.2)',
          ],
          borderColor: [
            'rgba(255, 99, 132, 1)',
            'rgba(54, 162, 235, 1)',
            'rgba(255, 206, 86, 1)',
            'rgba(75, 192, 192, 1)',
            'rgba(153, 102, 255, 1)',
            'rgba(255, 159, 64, 1)',
          ],

        }
    ]
  }
}

export function Graph(props) {
  return <Grid container>
    <Grid item xs = {12}><Typography variant='h2' component={"h2"} textAlign={'center'}> Adultos </Typography></Grid>
    <Grid></Grid>
    <Grid item xs = {8}> <Pie data={createGraph(props.data)} />  </Grid>

  </Grid>
}


